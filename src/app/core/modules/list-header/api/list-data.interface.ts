export interface IListData {

    /**
     * selected items
     *
     * @type {number}
     * @memberof IListData
     */
    selected: number;

    /**
     * currently showing items
     *
     * @type {number}
     * @memberof IListData
     */
    showing: number;

    /**
     * total items
     *
     * @type {number}
     * @memberof IListData
     */
    total: number;
}
